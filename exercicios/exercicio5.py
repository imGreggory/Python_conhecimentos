#Fazer um programa que leia um numero e fale o numero anterior e o posterior a ele:

numero = int(input("Digite um número:"))
numeroPosterior = numero + 1
numeroAnterior = numero - 1

print("O seu número é: {}, o número depois dele é: {} e o número anterior a ele é: {}".format(numero,numeroPosterior,numeroAnterior))


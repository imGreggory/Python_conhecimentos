largura = float(input("Escreva a largura da parede: "))
altura = float(input("Escreva a altura da parede: "))
area = largura * altura
tinta = area / 2


print(f"A área da parede é de: {area} metros, e a quantidade de tinta necessária para pinta-la é de {tinta} litros.")

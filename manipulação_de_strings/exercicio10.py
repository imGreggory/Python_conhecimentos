#Crie um algoritmo que leia um numero, mostre seu valor em dólares:

#variáveis
valorreais = int(input("Escreva seu valor em reais: "))
valordolares = float(input("Escreva quanto está o dólar hoje:"))
valorconvertido = valorreais * valordolares


#código
print(f"Seu valor em reais é de {valorreais} reais, com o dólar a {valordolares}, o seu valor em dólares fica: {valorconvertido}")
#Converter graus Celsius em Farenheit

grausCelsius = float(input("Escreva a temperatura em graus Celsius: "))
grausFarenheit = (grausCelsius * 9/5) + 32

print(f"Sua temperatura de {grausCelsius}º graus celsius é equivalente a {grausFarenheit}º graus farenheit.")